               <h1>Historia</h1>
               <h2>Cómo nació Grupo Norte</h2>
               <p>Transcurridos los primeros años del nuevo milenio, allá por 2004, 2005, tres empresarios que vivían y aún viven en la Zona Norte de la ciudad, todos ellos inquietos participes de cuanta institución intermedia los convoca, soñaban con la idea de constituir un Foro que permitiera que los líderes de la comunidad que estaban afincados o tenían sus intereses en esa zona, tuvieran un espacio para poder expresarse y hacer conocer sus ideas.</p>

               <p>En el corazón de Roberto Lázzaro, Miguel Milano y Carlos Prestipino anidaba el sueño de construir un ámbito de reflexión y acción en el que resonaran todas las voces y que fuera un interlocutor respetado y escuchado por los poderes políticos de turno, con el 
único interés de ayudar a pensar que barrio, que ciudad, que región; en definitiva, que país queríamos para los próximos años, o para decirlo de otra manera, deseaban colaborar en el diseño del mundo en el que viven sus hijos e iban a vivir sus nietos.</p>
               <p>Fue así como un 15 de noviembre del año 2006, día aciago en la memoria de los rosarinos, ya que lo recordaremos además por el intenso granizo que destruyó gran parte de la ciudad, se autoconvocaron a compartir una cena en el Restaurant "Señor Arenero", en la Costanera, comprometiéndose cada uno a acercar a algunos amigos o conocidos que coincidieran en ese perfil de ciudadanos preocupados y ocupados en la futuro de su comunidad.</p>

               <p>Fue una cena larga y distendida, donde cada uno se presentó, contó su historia y permitió que los demás lo “descubrieran”, ya que a pesar de ser vecinos, muchos no se conocían entre sí. Participaron de esa cena inaugural, los tres antes mencionados a los que se sumaron Norberto Ruggiero, Rubén Quargnul, Mario Cornaglia, Andrés Panero, Jorge Baio, Lorenzo Fernández, Oscar Jacobson, Fernando Jacobson, Carlos Rios, Marina Villalonga y Enrique Humanes.</p>

               <p>Desde entonces, cada primer miércoles de mes, se reúnen para celebrar su condición de vecinos enfocados en las necesidades de la comunidad, para reforzar los lazos de afecto y ayudar a diseñar un futuro de prosperidad para el barrio y para Rosario. Algunos, ya no están, otros se han incorporado, pero lo cierto es que con más de 30 participantes activos, Grupo Norte está escribiendo hoy, a 9 años de su creación, una de las páginas más brillantes de su corta pero fecunda historia. </p>

